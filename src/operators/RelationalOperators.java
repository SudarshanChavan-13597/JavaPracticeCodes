package operators;

public class RelationalOperators {

	public static void main(String[] args) {
		// Relational operator
				//   <,>,<=,>=,==,!=
				
				int a = 8;
				int b = 4;
				
				boolean c= a<b;
				System.out.println(c);
				
				boolean d= a>b;
				System.out.println(d);
				
				boolean e= a<=b;
				System.out.println(e);
				
				boolean f= a>=b;
				System.out.println(f);
	}

}
